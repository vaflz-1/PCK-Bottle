export function normalizeLocale(locale) {
  const normalized = String(locale ?? "").toLowerCase().split("-")[0];
  return LOCALES.includes(normalized) ? normalized : "en";
}

export function getLabels(locale) {
  return LABELS[normalizeLocale(locale)];
}

export function translate(locale, key) {
  const labels = getLabels(locale);
  return labels[key] ?? LABELS.en[key] ?? key;
}
